package org.GCGA.client.utility;

/**
 *
 * @author Owner
 */
public class DoublePoint {
    public double x;
    public double y;

    public DoublePoint(Double ix, Double iy) {
        x = ix;
        y = iy;
    }

    public DoublePoint() {
        x = 0;
        y = 0;
    }


}
